import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { SITE_ORIGIN, SITE_ROUTES, rewriteLegacyLinks } from './site-routes.mjs';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

for (const page of SITE_ROUTES) {
  const sourcePath = resolve(root, page.source);
  const source = await readFile(sourcePath, 'utf8');
  const normalized = rewriteLegacyLinks(source);

  if (normalized !== source) await writeFile(sourcePath, normalized);
  if (!page.route) continue;

  const depth = page.route.split('/').filter(Boolean).length;
  const baseHref = '../'.repeat(depth);
  const cleanHtml = normalized
    .replace('<!doctype html>', '<!doctype html>\n<!-- CLEAN URL copy generated by scripts/generate-clean-urls.mjs -->')
    .replace('<head>', `<head>\n<base href="${baseHref}">`);
  const outputDir = resolve(root, page.route);
  await mkdir(outputDir, { recursive: true });
  await writeFile(resolve(outputDir, 'index.html'), cleanHtml);
}

const sitemapEntries = SITE_ROUTES.map(page => `  <url>
    <loc>${SITE_ORIGIN}/${page.route}</loc>
    <changefreq>${page.changefreq}</changefreq>
    <priority>${page.priority}</priority>
  </url>`).join('\n\n');

await writeFile(resolve(root, 'sitemap.xml'), `<?xml version="1.0" encoding="UTF-8"?>
<!-- Карта сайта для Яндекс и Google. -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">

${sitemapEntries}

</urlset>
`);

console.log(`Generated ${SITE_ROUTES.length - 1} clean URL pages and sitemap.xml.`);
